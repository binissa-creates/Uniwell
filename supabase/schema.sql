-- ── Enum types ───────────────────────────────────────────────────────────────
do $$
begin
  if not exists (select 1 from pg_type where typname = 'user_role') then
    create type public.user_role as enum ('student', 'admin');
  end if;
  if not exists (select 1 from pg_type where typname = 'mood_type') then
    create type public.mood_type as enum ('rad', 'good', 'meh', 'bad', 'awful');
  end if;
  if not exists (select 1 from pg_type where typname = 'trigger_category') then
    create type public.trigger_category as enum (
      'Academics', 'Social', 'Family', 'Health',
      'Finance', 'Relationships', 'Personal Growth', 'Other'
    );
  end if;
  if not exists (select 1 from pg_type where typname = 'coping_category') then
    create type public.coping_category as enum (
      'Relaxation', 'Time Management', 'Social Support',
      'Physical Activity', 'Creative Expression', 'Mindfulness', 'Other'
    );
  end if;
  if not exists (select 1 from pg_type where typname = 'coping_status') then
    create type public.coping_status as enum ('pending', 'approved', 'rejected');
  end if;
end$$;

-- ── Profiles (1:1 with auth.users) ───────────────────────────────────────────
create table if not exists public.profiles (
  id          uuid primary key references auth.users(id) on delete cascade,
  name        varchar(150) not null,
  student_id  varchar(50)  not null unique,
  course      varchar(100) not null,
  year_level  smallint     not null check (year_level between 1 and 4),
  role        public.user_role not null default 'student',
  created_at  timestamptz  not null default now()
);

create index if not exists profiles_role_idx on public.profiles (role);

-- Auto-create a profile when an auth user is created, reading metadata supplied
-- at sign-up time via options.data.
create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, name, student_id, course, year_level, role)
  values (
    new.id,
    coalesce(new.raw_user_meta_data ->> 'name', split_part(new.email, '@', 1)),
    coalesce(new.raw_user_meta_data ->> 'student_id', new.id::text),
    coalesce(new.raw_user_meta_data ->> 'course', 'Unspecified'),
    coalesce((new.raw_user_meta_data ->> 'year_level')::smallint, 1),
    coalesce((new.raw_user_meta_data ->> 'role')::public.user_role, 'student')
  );
  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();

-- Convenience: is_admin() helper for RLS policies.
create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1 from public.profiles p
    where p.id = auth.uid() and p.role = 'admin'
  );
$$;

grant execute on function public.is_admin() to authenticated;

-- ── Mood logs ────────────────────────────────────────────────────────────────
create table if not exists public.mood_logs (
  id         bigint generated by default as identity primary key,
  user_id    uuid not null references public.profiles(id) on delete cascade,
  mood_type  public.mood_type not null,
  intensity  smallint not null default 3 check (intensity between 1 and 5),
  note       text,
  logged_at  timestamptz not null default now()
);

create index if not exists mood_logs_user_logged_at_idx
  on public.mood_logs (user_id, logged_at desc);

-- ── Mood triggers ────────────────────────────────────────────────────────────
create table if not exists public.mood_triggers (
  id               bigint generated by default as identity primary key,
  log_id           bigint not null references public.mood_logs(id) on delete cascade,
  trigger_category public.trigger_category not null
);

create index if not exists mood_triggers_log_id_idx on public.mood_triggers (log_id);

-- ── Journal entries ──────────────────────────────────────────────────────────
create table if not exists public.journal_entries (
  id         bigint generated by default as identity primary key,
  user_id    uuid not null references public.profiles(id) on delete cascade,
  content    text not null,
  prompt     text,
  created_at timestamptz not null default now()
);

create index if not exists journal_entries_user_created_at_idx
  on public.journal_entries (user_id, created_at desc);

-- ── Coping strategies ────────────────────────────────────────────────────────
create table if not exists public.coping_strategies (
  id            bigint generated by default as identity primary key,
  submitter_id  uuid not null references public.profiles(id) on delete cascade,
  category      public.coping_category not null,
  title         varchar(200) not null,
  description   text not null,
  trigger_tags  jsonb not null default '[]'::jsonb,
  status        public.coping_status not null default 'pending',
  helpful_count integer not null default 0,
  created_at    timestamptz not null default now()
);

create index if not exists coping_strategies_feed_idx
  on public.coping_strategies (status, helpful_count desc, created_at desc);

create index if not exists coping_strategies_trigger_tags_idx
  on public.coping_strategies using gin (trigger_tags);

-- ── Helpful votes ────────────────────────────────────────────────────────────
create table if not exists public.helpful_votes (
  id          bigint generated by default as identity primary key,
  strategy_id bigint not null references public.coping_strategies(id) on delete cascade,
  user_id     uuid not null references public.profiles(id) on delete cascade,
  voted_at    timestamptz not null default now(),
  constraint helpful_votes_strategy_user_unique unique (strategy_id, user_id)
);

