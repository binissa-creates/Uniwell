-- Grants — RLS is the gate, so grant CRUD then restrict via policies.
grant usage on schema public to anon, authenticated;
grant select on public.profiles          to authenticated;
grant select, insert, update, delete on public.mood_logs         to authenticated;
grant select, insert, update, delete on public.mood_triggers     to authenticated;
grant select, insert, update, delete on public.journal_entries   to authenticated;
grant select, insert, update, delete on public.coping_strategies to authenticated;
grant select, insert,        delete on public.helpful_votes      to authenticated;
grant usage, select on all sequences in schema public to authenticated;

-- profiles: read own + anyone can read displayed names for approved coping;
-- admins can read all; updates via functions/triggers only.
drop policy if exists profiles_select on public.profiles;
create policy profiles_select on public.profiles
  for select to authenticated
  using (id = auth.uid() or public.is_admin() or role = 'student');

drop policy if exists profiles_update_self on public.profiles;
create policy profiles_update_self on public.profiles
  for update to authenticated
  using (id = auth.uid() or public.is_admin())
  with check (id = auth.uid() or public.is_admin());

-- mood_logs: student owns their rows; admin reads all.
drop policy if exists mood_logs_select on public.mood_logs;
create policy mood_logs_select on public.mood_logs
  for select to authenticated
  using (user_id = auth.uid() or public.is_admin());

drop policy if exists mood_logs_insert on public.mood_logs;
create policy mood_logs_insert on public.mood_logs
  for insert to authenticated
  with check (user_id = auth.uid());

drop policy if exists mood_logs_delete on public.mood_logs;
create policy mood_logs_delete on public.mood_logs
  for delete to authenticated
  using (user_id = auth.uid());

-- mood_triggers: follow parent log's ownership.
drop policy if exists mood_triggers_select on public.mood_triggers;
create policy mood_triggers_select on public.mood_triggers
  for select to authenticated
  using (
    exists (
      select 1 from public.mood_logs ml
      where ml.id = mood_triggers.log_id
        and (ml.user_id = auth.uid() or public.is_admin())
    )
  );

drop policy if exists mood_triggers_insert on public.mood_triggers;
create policy mood_triggers_insert on public.mood_triggers
  for insert to authenticated
  with check (
    exists (
      select 1 from public.mood_logs ml
      where ml.id = mood_triggers.log_id and ml.user_id = auth.uid()
    )
  );

-- journal_entries: purely private; admin does NOT get read access.
drop policy if exists journal_select_own on public.journal_entries;
create policy journal_select_own on public.journal_entries
  for select to authenticated
  using (user_id = auth.uid());

drop policy if exists journal_insert_own on public.journal_entries;
create policy journal_insert_own on public.journal_entries
  for insert to authenticated
  with check (user_id = auth.uid());

drop policy if exists journal_delete_own on public.journal_entries;
create policy journal_delete_own on public.journal_entries
  for delete to authenticated
  using (user_id = auth.uid());

-- coping_strategies: approved visible to all authenticated; own + admin see pending/rejected.
drop policy if exists coping_select on public.coping_strategies;
create policy coping_select on public.coping_strategies
  for select to authenticated
  using (
    status = 'approved'
    or submitter_id = auth.uid()
    or public.is_admin()
  );

drop policy if exists coping_insert_own on public.coping_strategies;
create policy coping_insert_own on public.coping_strategies
  for insert to authenticated
  with check (submitter_id = auth.uid() and status = 'pending');

-- Admin moderation: only admins can flip status.
drop policy if exists coping_update_admin on public.coping_strategies;
create policy coping_update_admin on public.coping_strategies
  for update to authenticated
  using (public.is_admin())
  with check (public.is_admin());

