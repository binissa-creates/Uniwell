-- Log a mood + triggers atomically.
create or replace function public.log_mood(
  p_mood_type public.mood_type,
  p_intensity smallint,
  p_note text,
  p_triggers public.trigger_category[]
)
returns bigint
language plpgsql
security invoker
set search_path = public
as $$
declare
  v_log_id bigint;
begin
  insert into public.mood_logs (user_id, mood_type, intensity, note)
  values (auth.uid(), p_mood_type, p_intensity, p_note)
  returning id into v_log_id;

  if p_triggers is not null and array_length(p_triggers, 1) > 0 then
    insert into public.mood_triggers (log_id, trigger_category)
    select v_log_id, t from unnest(p_triggers) as t;
  end if;

  return v_log_id;
end;
$$;

grant execute on function public.log_mood(public.mood_type, smallint, text, public.trigger_category[])
  to authenticated;

-- Toggle a helpful vote + keep helpful_count in sync.
create or replace function public.toggle_helpful_vote(p_strategy_id bigint)
returns boolean
language plpgsql
security invoker
set search_path = public
as $$
declare
  v_uid uuid := auth.uid();
  v_exists boolean;
begin
  select exists(
    select 1 from public.helpful_votes
    where strategy_id = p_strategy_id and user_id = v_uid
  ) into v_exists;

  if v_exists then
    delete from public.helpful_votes
      where strategy_id = p_strategy_id and user_id = v_uid;
    update public.coping_strategies
      set helpful_count = greatest(0, helpful_count - 1)
      where id = p_strategy_id;
    return false;
  else
    insert into public.helpful_votes (strategy_id, user_id)
    values (p_strategy_id, v_uid);
    update public.coping_strategies
      set helpful_count = helpful_count + 1
      where id = p_strategy_id;
    return true;
  end if;
end;
$$;

grant execute on function public.toggle_helpful_vote(bigint) to authenticated;

-- Admin analytics aggregate. SECURITY DEFINER so it can scan all users
-- while still gating via is_admin() check.
create or replace function public.admin_analytics(
  p_days integer default 30,
  p_year_level integer default null,
  p_course text default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_since timestamptz := now() - (p_days * interval '1 day');
  v_result jsonb;
begin
  if not public.is_admin() then
    raise exception 'forbidden' using errcode = '42501';
  end if;

  with base as (
    select ml.*, p.year_level, p.course
    from public.mood_logs ml
    join public.profiles p on p.id = ml.user_id
    where ml.logged_at >= v_since
      and (p_year_level is null or p.year_level = p_year_level)
      and (p_course is null or p.course = p_course)
  )
  select jsonb_build_object(
    'totalStudents', (select count(distinct user_id)::int from base),
    'moodDistribution', coalesce((
      select jsonb_agg(x) from (
        select mood_type, count(*)::int as count
        from base group by mood_type
      ) x
    ), '[]'::jsonb),
    'dailyTrend', coalesce((
      select jsonb_agg(x order by x->>'date') from (
        select jsonb_build_object(
          'date', (logged_at at time zone 'UTC')::date,
          'mood_type', mood_type,
          'count', count(*)::int
        ) as x
        from base
        group by (logged_at at time zone 'UTC')::date, mood_type
      ) y
    ), '[]'::jsonb),
    'topTriggers', coalesce((
      select jsonb_agg(x order by (x->>'count')::int desc) from (
        select jsonb_build_object(
          'trigger_category', mt.trigger_category,
          'count', count(*)::int
        ) as x
        from public.mood_triggers mt
        join base on base.id = mt.log_id
        group by mt.trigger_category
      ) y
    ), '[]'::jsonb),
    'byYearLevel', coalesce((
      select jsonb_agg(x order by (x->>'year_level')::int) from (
        select jsonb_build_object(
          'year_level', year_level,
          'mood_type', mood_type,
          'count', count(*)::int
        ) as x
        from base group by year_level, mood_type
      ) y
    ), '[]'::jsonb),
    'byCourse', coalesce((
      select jsonb_agg(x order by x->>'course') from (
        select jsonb_build_object(
          'course', course,
          'mood_type', mood_type,
          'count', count(*)::int
        ) as x
        from base group by course, mood_type
      ) y
    ), '[]'::jsonb)
  ) into v_result;

  return v_result;
end;
$$;

grant execute on function public.admin_analytics(integer, integer, text) to authenticated;

