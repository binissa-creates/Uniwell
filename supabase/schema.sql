-- =========================================
-- UniWell Database Schema (Supabase client architecture)
-- Run once in the Supabase SQL Editor on an empty project.
--
-- Architecture: the React app talks to Supabase directly via supabase-js.
-- Auth lives in Supabase Auth (auth.users, UUIDs). App data lives in public.*
-- and is protected by RLS — no Express server in the middle.
